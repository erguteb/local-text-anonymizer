def parse_keyword_list(raw_keywords: Optional[str]) -> List[str]:
    """Parse comma-separated keywords into a clean list."""
    if not raw_keywords or not raw_keywords.strip():
        return []
    return [part.strip() for part in raw_keywords.split(",") if part.strip()]


def parse_model_list(raw_models: Optional[str]) -> List[str]:
    """Parse comma-separated model names into a clean list."""
    if not raw_models or not raw_models.strip():
        return []
    return [part.strip() for part in raw_models.split(",") if part.strip()]


def parse_removal_list(raw_removals: Optional[str]) -> List[str]:
    """Parse comma-separated removal targets into a clean list."""

    if not raw_removals or not raw_removals.strip():
        return []
    return [part.strip() for part in raw_removals.split(",") if part.strip()]


def normalize_whitespace(text: str) -> str:
    """Collapse repeated whitespace without changing sentence punctuation."""

    return re.sub(r"\s+", " ", text).strip()


def dedupe_preserve_order(values: Sequence[str]) -> List[str]:
    """Deduplicate string values while preserving their order."""

    seen = set()
    output: List[str] = []
    for value in values:
        cleaned = value.strip()
        if not cleaned:
            continue
        lowered = cleaned.lower()
        if lowered in seen:
            continue
        seen.add(lowered)
        output.append(cleaned)
    return output


def regex_sub_with_placeholder(text: str, pattern: str, placeholder: str) -> str:
    """Replace regex matches with a stable placeholder token."""

    return re.sub(pattern, placeholder, text, flags=re.IGNORECASE)


def expand_category_removal_targets(text: str, removal_targets: List[str]) -> List[str]:
    """
    Expand category-level removal requests into concrete or typed targets.

    The downstream scrubber understands the bracketed typed placeholders below.
    """

    expanded: List[str] = []
    for target in removal_targets:
        lowered = target.strip().lower()
        if not lowered:
            continue
        if lowered in {"all names", "names", "name"}:
            expanded.append("[PERSON]")
            continue
        if lowered in {"all ages", "ages", "age"}:
            expanded.append("[AGE]")
            continue
        if lowered in {
            "all employers",
            "employers",
            "employer",
            "company",
            "all companies",
            "organization",
            "all organizations",
        }:
            expanded.append("[ORG]")
            continue
        if lowered in {
            "all addresses",
            "address",
            "all locations",
            "location",
            "all neighborhoods",
            "neighborhood",
        }:
            expanded.append("[LOCATION]")
            continue
        expanded.append(target)
    return dedupe_preserve_order(expanded)


def infer_placeholder_for_target(target: str) -> str:
    """
    Pick a generic placeholder token for a removal target.

    Heuristic mapping:
    - person-like names -> "someone"
    - location-like terms -> "somewhere"
    - default -> "something"
    """

    t = target.strip()
    if not t:
        return "something"

    # Simple person-name heuristic: 2+ title-cased words (e.g., "John Smith").
    words = [w for w in re.split(r"\s+", t) if w]
    if len(words) >= 2 and all(re.match(r"^[A-Z][a-zA-Z'-]*$", w) for w in words):
        return "someone"

    # Location-like hints for common place words/abbreviations.
    lower = t.lower()
    location_hints = {
        "city",
        "town",
        "village",
        "neighborhood",
        "neighbourhood",
        "flat",
        "apartment",
        "street",
        "road",
        "avenue",
        "district",
        "country",
        "state",
        "london",
        "uk",
        "u.k.",
        "england",
        "usa",
        "u.s.",
        "u.s.a.",
        "europe",
        "asia",
        "africa",
    }
    if any(hint in lower for hint in location_hints):
        return "somewhere"

    location_suffixes = ("ditch", "ham", "ton", "ford", "bury", "bridge", "field", "side")
    if len(words) == 1 and lower.endswith(location_suffixes):
        return "somewhere"

    return "something"


def replace_targets_literal(text: str, removal_targets: List[str]) -> str:
    """
    Replace target phrases directly in text (case-insensitive), preserving structure.
    """

    updated = text
    for target in removal_targets:
        target_clean = target.strip()
        if not target_clean:
            continue
        if target_clean == "[AGE]":
            updated = regex_sub_with_placeholder(
                updated,
                r"\b(?:age\s*\d{1,3}|\d{1,3}\s*(?:years old|year-old)|i\s*[\'’]?m\s*\d{1,3})\b",
                "[AGE]",
            )
            continue
        if target_clean == "[PERSON]":
            updated = re.sub(
                r"\b(?:[A-Z][a-z]+(?:\s+[A-Z][a-z]+)+)\b",
                "[PERSON]",
                updated,
            )
            continue
        if target_clean == "[ORG]":
            updated = re.sub(
                r"\b(?:[A-Z][\w&'-]+(?:\s+[A-Z][\w&'-]+){0,3}\s+(?:Inc|LLC|Ltd|Corp|Company|Studio|Agency|Enterprises))\b",
                "[ORG]",
                updated,
            )
            continue
        if target_clean == "[LOCATION]":
            updated = re.sub(
                r"\b(?:flat|apartment|suite|room)?\s*\d+\s+[A-Z][\w'-]+(?:\s+[A-Z][\w'-]+){0,3}\b",
                "[LOCATION]",
                updated,
            )
            updated = re.sub(
                r"\b(?:[A-Z][a-z]+(?:ditch|ham|ton|ford|bury|bridge|field|side))\b",
                "[LOCATION]",
                updated,
            )
            continue
        placeholder = infer_placeholder_for_target(target_clean)
        pattern = re.compile(re.escape(target_clean), flags=re.IGNORECASE)
        updated = pattern.sub(placeholder, updated)
    return updated


def split_sentences_by_period(text: str) -> List[str]:
    """
    Split into sentence-like spans with simple punctuation-aware heuristics.
    """

    normalized = normalize_whitespace(text)
    if not normalized:
        return []
    parts = re.split(r"(?<=[.!?])\s+|\n+", normalized)
    return [part.strip() for part in parts if part.strip()]


def chunk_sentences_for_privacy(sentences: Sequence[str], max_chunks: int) -> List[str]:
    """Combine neighboring sentences to limit the number of DP releases."""

    items = [s.strip() for s in sentences if s and s.strip()]
    if not items:
        return []
    if max_chunks <= 0 or len(items) <= max_chunks:
        return list(items)

    chunk_size = max(1, math.ceil(len(items) / max_chunks))
    chunks: List[str] = []
    for start in range(0, len(items), chunk_size):
        chunk = " ".join(items[start : start + chunk_size]).strip()
        if chunk:
            chunks.append(chunk)
    return chunks[:max_chunks] if len(chunks) > max_chunks else chunks


def classify_public_keywords(keywords: Sequence[str]) -> Tuple[List[str], List[str], List[str]]:
    """Split keywords into task/location/preference buckets."""

    task_keywords: List[str] = []
    location_keywords: List[str] = []
    preference_keywords: List[str] = []
    for keyword in dedupe_preserve_order(list(keywords)):
        lower = keyword.lower()
        if lower in {"uk", "u.k.", "england", "london"}:
            location_keywords.append(keyword)
        elif any(token in lower for token in ("restaurant", "dining", "food", "cafe", "bar")):
            task_keywords.append(keyword)
        else:
            preference_keywords.append(keyword)
    return task_keywords, location_keywords, preference_keywords


def placeholder_for_safe_keyword(keyword: str) -> str:
    """Return a stable placeholder bucket for a preserved public keyword."""

    task_keywords, location_keywords, _ = classify_public_keywords([keyword])
    if location_keywords:
        return "[SAFE_LOCATION]"
    if task_keywords:
        return "[SAFE_TASK]"
    return "[SAFE_PREFERENCE]"


def strip_safe_slot_placeholders(text: str) -> str:
    """Drop safe-slot placeholders from residual summaries before final rendering."""

    cleaned = re.sub(r"\[SAFE_[A-Z_]+\]", "", text)
    cleaned = re.sub(r"\s+([,.;:!?])", r"\1", cleaned)
    return normalize_whitespace(cleaned)


def restore_safe_slot_placeholders(
    text: str,
    *,
    safe_task_keywords: Sequence[str],
    safe_location_keywords: Sequence[str],
    safe_preference_keywords: Sequence[str],
) -> str:
    """Restore safe-slot placeholders using canonical preserved values."""

    restored = text
    task_value = safe_task_keywords[0] if safe_task_keywords else ""
    preference_value = safe_preference_keywords[0] if safe_preference_keywords else ""
    location_value = ""
    if safe_location_keywords:
        generic_locations = {"uk", "u.k.", "england"}
        specific_locations = [
            value for value in safe_location_keywords if value.strip().lower() not in generic_locations
        ]
        location_value = (specific_locations or list(safe_location_keywords))[0]
    replacements = {
        "[SAFE_TASK]": task_value,
        "[SAFE_LOCATION]": location_value,
        "[SAFE_PREFERENCE]": preference_value,
    }
    for placeholder, value in replacements.items():
        if value:
            restored = restored.replace(placeholder, value)
        else:
            restored = restored.replace(placeholder, "")
    restored = re.sub(r"\s+([,.;:!?])", r"\1", restored)
    restored = re.sub(r"\b(a)\s+([aeiouAEIOU])", r"an \2", restored)
    return normalize_whitespace(restored)


def redact_preserved_keywords_from_residual(text: str, keywords: Sequence[str]) -> str:
    """
    Replace explicitly allowed public keywords with stable placeholders.

    This keeps those slots out of the DP-protected residual path while allowing
    the final renderer to re-introduce them deterministically.
    """

    redacted = text
    sorted_keywords = sorted(
        dedupe_preserve_order(list(keywords)),
        key=lambda keyword: len(keyword),
        reverse=True,
    )
    for keyword in sorted_keywords:
        if not keyword.strip():
            continue
        placeholder = placeholder_for_safe_keyword(keyword)
        redacted = re.sub(
            re.escape(keyword),
            placeholder,
            redacted,
            flags=re.IGNORECASE,
        )
    return normalize_whitespace(redacted)


def preprocess_privacy_text(
    text: str,
    *,
    keywords: Sequence[str],
    removal_targets: Sequence[str],
    max_privacy_chunks: int,
) -> PreprocessedPrivacyText:
    """
    Deterministically scrub explicit sensitive spans before embedding inversion.

    Keywords are intentionally preserved as public utility slots and are not
    counted inside the DP mechanism.
    """

    expanded_targets = expand_category_removal_targets(text, list(removal_targets))
    scrubbed = replace_targets_literal(text, expanded_targets)
    scrubbed = normalize_whitespace(scrubbed)
    preserved_keywords = dedupe_preserve_order(list(keywords))
    safe_task_keywords, safe_location_keywords, safe_preference_keywords = classify_public_keywords(
        preserved_keywords
    )
    residual_text = redact_preserved_keywords_from_residual(scrubbed, preserved_keywords)
    residual_segments = chunk_sentences_for_privacy(
        split_sentences_by_period(residual_text),
        max_chunks=max_privacy_chunks,
    )
    residual_joined = " ".join(residual_segments).strip()
    return PreprocessedPrivacyText(
        original_text=text,
        scrubbed_text=scrubbed,
        residual_text=residual_joined,
        expanded_removal_targets=expanded_targets,
        preserved_keywords=preserved_keywords,
        safe_task_keywords=safe_task_keywords,
        safe_location_keywords=safe_location_keywords,
        safe_preference_keywords=safe_preference_keywords,
        residual_segments=residual_segments,
    )


def estimate_metric_dp_privacy_cost(
    *,
    num_releases: int,
    sigma: float,
    delta: float,
    sensitivity: float = 1.0,
) -> PrivacyAccountantResult:
    """
    Estimate overall privacy cost for sentence-level metric-DP perturbation.

    Assumptions:
    - One Gaussian perturbation release per sentence.
    - Same sigma used for every sentence.
    - L2 sensitivity of embedding function is provided by user (default 1.0).
    - Composition handled via zCDP, then converted to (epsilon, delta)-DP.
    """

    releases = max(0, int(num_releases))
    s = float(sensitivity)
    sig = float(sigma)
    d = float(delta)

    if d <= 0.0 or d >= 1.0:
        raise ValueError("dp-delta must be in (0, 1).")
    if s <= 0.0:
        raise ValueError("dp-sensitivity must be > 0.")

    if releases == 0:
        return PrivacyAccountantResult(
            num_releases=0,
            sigma=sig,
            delta=d,
            sensitivity=s,
            rho_single=0.0,
            rho_total=0.0,
            epsilon=0.0,
        )

    if sig <= 0.0:
        # No noise => no finite DP guarantee.
        return PrivacyAccountantResult(
            num_releases=releases,
            sigma=sig,
            delta=d,
            sensitivity=s,
            rho_single=float("inf"),
            rho_total=float("inf"),
            epsilon=float("inf"),
        )

    rho_single = (s * s) / (2.0 * sig * sig)
    rho_total = float(releases) * rho_single
    epsilon = rho_total + 2.0 * math.sqrt(rho_total * math.log(1.0 / d))

    return PrivacyAccountantResult(
        num_releases=releases,
        sigma=sig,
        delta=d,
        sensitivity=s,
        rho_single=rho_single,
        rho_total=rho_total,
        epsilon=epsilon,
    )


