def patch_transformers_constrained_beamsearch_4372() -> None:
    """
    Patch transformers constrained beam-search finalize() for 4.37.2.

    Why this exists:
    Vec2Text generation with constrained decoding can hit:
      RuntimeError: Could not infer dtype of NoneType
    when some hypotheses have `best_idx=None`.
    """

    import transformers
    from collections import UserDict
    from transformers.generation.beam_search import ConstrainedBeamSearchScorer

    if getattr(ConstrainedBeamSearchScorer.finalize, "_vec2text_patched", False):
        return

    if transformers.__version__ != "4.37.2":
        print(
            f"[patch] Warning: transformers=={transformers.__version__}. "
            "Patch was written for 4.37.2."
        )

    def finalize_patched(
        self,
        input_ids: torch.LongTensor,
        final_beam_scores: torch.FloatTensor,
        final_beam_tokens: torch.LongTensor,
        final_beam_indices: torch.LongTensor,
        max_length: int,
        pad_token_id: Optional[int] = None,
        eos_token_id: Optional[int | List[int]] = None,
        beam_indices: Optional[torch.LongTensor] = None,
        decoder_prompt_len: int = 0,
    ) -> Any:
        batch_size = len(self._beam_hyps)
        if isinstance(eos_token_id, int):
            eos_token_id = [eos_token_id]

        for batch_idx, beam_hyp in enumerate(self._beam_hyps):
            if self._done[batch_idx]:
                continue

            ids_collect: List[int] = []
            for beam_id in range(self.num_beams):
                batch_beam_idx = batch_idx * self.num_beams + beam_id
                final_score = final_beam_scores[batch_beam_idx].item()
                final_tokens = input_ids[batch_beam_idx]
                completes_constraint = self.check_completes_constraints(
                    final_tokens.cpu().tolist()
                )
                if completes_constraint:
                    beam_index = beam_indices[batch_beam_idx] if beam_indices is not None else None
                    generated_len = final_tokens.shape[-1] - decoder_prompt_len
                    beam_hyp.add(
                        final_tokens,
                        final_score,
                        beam_indices=beam_index,
                        generated_len=generated_len,
                    )
                    ids_collect.append(beam_id)

            if len(ids_collect) < self.num_beam_hyps_to_keep:
                for beam_id in range(self.num_beams):
                    if beam_id not in ids_collect:
                        batch_beam_idx = batch_idx * self.num_beams + beam_id
                        final_score = final_beam_scores[batch_beam_idx].item()
                        final_tokens = input_ids[batch_beam_idx]
                        generated_len = final_tokens.shape[-1] - decoder_prompt_len
                        beam_hyp.add(final_tokens, final_score, generated_len=generated_len)
                    if len(ids_collect) >= self.num_beam_hyps_to_keep:
                        break

        sent_lengths = input_ids.new(batch_size * self.num_beam_hyps_to_keep)
        best: List[torch.LongTensor] = []
        best_indices: List[Optional[Sequence[int]]] = []
        best_scores = torch.zeros(
            batch_size * self.num_beam_hyps_to_keep,
            device=self.device,
            dtype=torch.float32,
        )

        for i, beam_hyp in enumerate(self._beam_hyps):
            sorted_hyps = sorted(beam_hyp.beams, key=lambda x: x[0])
            for j in range(self.num_beam_hyps_to_keep):
                best_hyp_tuple = sorted_hyps.pop()
                best_score = best_hyp_tuple[0]
                best_hyp = best_hyp_tuple[1]
                best_index = best_hyp_tuple[2]

                sent_lengths[self.num_beam_hyps_to_keep * i + j] = len(best_hyp)
                best.append(best_hyp)
                best_indices.append(best_index)
                best_scores[i * self.num_beam_hyps_to_keep + j] = best_score

        sent_lengths_max = sent_lengths.max().item() + 1
        sent_max_len = min(sent_lengths_max, max_length) if max_length is not None else sent_lengths_max
        decoded = input_ids.new(batch_size * self.num_beam_hyps_to_keep, sent_max_len)

        if len(best_indices) > 0 and any(idx is not None for idx in best_indices):
            indices = input_ids.new(batch_size * self.num_beam_hyps_to_keep, sent_max_len)
        else:
            indices = None

        if sent_lengths.min().item() != sent_lengths.max().item():
            if pad_token_id is None:
                raise ValueError("`pad_token_id` has to be defined")
            decoded.fill_(pad_token_id)
            if indices is not None:
                indices.fill_(-1)

        for i, (hypo, best_idx) in enumerate(zip(best, best_indices)):
            decoded[i, : sent_lengths[i]] = hypo
            if indices is not None and best_idx is not None:
                indices[i, : len(best_idx)] = torch.tensor(
                    best_idx,
                    device=indices.device,
                    dtype=indices.dtype,
                )
            if sent_lengths[i] < sent_max_len:
                decoded[i, sent_lengths[i]] = eos_token_id[0]

        return UserDict(
            {
                "sequences": decoded,
                "sequence_scores": best_scores,
                "beam_indices": indices,
            }
        )

    finalize_patched._vec2text_patched = True
    ConstrainedBeamSearchScorer.finalize = finalize_patched
    print("[patch] Patched ConstrainedBeamSearchScorer.finalize() for transformers 4.37.2.")


def load_vec2text_corrector(
    device: str,
    *,
    inversion_model_name: str = "jxm/gtr__nq__32",
    corrector_model_name: str = "jxm/gtr__nq__32__correct",
    cache_dir: Optional[str] = None,
) -> Any:
    """
    Load Vec2Text inversion+corrector pair.

    Returns:
        Corrector object used by vec2text.generate().
    """

    print("Loading vec2text models...")
    inversion_model = vec2text.models.InversionModel.from_pretrained(
        inversion_model_name,
        cache_dir=cache_dir,
    ).to(device)
    corrector_model = vec2text.models.CorrectorEncoderModel.from_pretrained(
        corrector_model_name,
        cache_dir=cache_dir,
    ).to(device)
    corrector = vec2text.load_corrector(inversion_model, corrector_model)
    corrector.inversion_trainer.model.to(device).eval()
    corrector.model.to(device).eval()
    print("Vec2text models loaded.")
    return corrector


@torch.no_grad()
def embed_texts(
    corrector: Any,
    texts: Sequence[str],
    *,
    max_length: int = 128,
    device: str = "cpu",
) -> torch.Tensor:
    """Encode text(s) into the same embedding space used by the vec2text checkpoint."""

    tok = corrector.embedder_tokenizer(
        list(texts),
        return_tensors="pt",
        max_length=max_length,
        truncation=True,
        padding="max_length",
    ).to(device)

    emb = corrector.inversion_trainer.call_embedding_model(
        input_ids=tok.input_ids,
        attention_mask=tok.attention_mask,
    )
    return emb


def add_gaussian_noise(emb: torch.Tensor, noise_std: float, seed: int = 0) -> torch.Tensor:
    """Add isotropic Gaussian noise to an embedding tensor."""

    if noise_std <= 0:
        return emb
    generator = torch.Generator(device="cpu")
    generator.manual_seed(seed)
    noise = torch.randn(emb.shape, generator=generator, dtype=emb.dtype)
    return emb + noise_std * noise.to(emb.device)


def l2_normalize(x: torch.Tensor, eps: float = 1e-12) -> torch.Tensor:
    """L2-normalize each row vector."""

    return x / torch.clamp(x.norm(p=2, dim=-1, keepdim=True), min=eps)


@torch.no_grad()
def mix_embeddings(
    base_embedding: torch.Tensor,
    keyword_embeddings: torch.Tensor,
    *,
    keyword_weights: Optional[List[float]] = None,
    keyword_total_weight: float = 0.35,
    normalize_each: bool = True,
    normalize_output: bool = True,
) -> Tuple[torch.Tensor, float, torch.Tensor]:
    """
    Mix noisy base embedding and keyword embeddings.

    Formula:
        e_mix = normalize(w_base * norm(e_base) + sum_k w_k * norm(e_kw_k))
    """

    if base_embedding.dim() != 2 or base_embedding.shape[0] != 1:
        raise ValueError("base_embedding must have shape (1, D)")
    if keyword_embeddings.dim() != 2 or keyword_embeddings.shape[0] < 1:
        raise ValueError("keyword_embeddings must have shape (K, D), K>=1")
    if base_embedding.shape[1] != keyword_embeddings.shape[1]:
        raise ValueError("base and keyword embeddings must have the same dimension")

    k_count = keyword_embeddings.shape[0]
    dev = base_embedding.device
    dtype = base_embedding.dtype

    if keyword_weights is None:
        w_kw = torch.full(
            (k_count,),
            float(keyword_total_weight) / float(k_count),
            device=dev,
            dtype=dtype,
        )
    else:
        if len(keyword_weights) != k_count:
            raise ValueError(f"keyword_weights length {len(keyword_weights)} != K={k_count}")
        w_kw = torch.tensor(keyword_weights, device=dev, dtype=dtype)

    w_kw_sum = float(w_kw.sum().item())
    if w_kw_sum > 1.0 + 1e-6:
        raise ValueError(f"Sum(keyword_weights)={w_kw_sum:.4f} > 1.0")
    w_base = 1.0 - w_kw_sum

    base_part = l2_normalize(base_embedding) if normalize_each else base_embedding
    kw_part = l2_normalize(keyword_embeddings) if normalize_each else keyword_embeddings
    kw_sum = (w_kw[:, None] * kw_part).sum(dim=0)
    mixed = w_base * base_part + kw_sum.unsqueeze(0)

    if normalize_output:
        mixed = l2_normalize(mixed)

    return mixed, w_base, w_kw


def optimize_joint_target_embedding(
    noisy_embedding: torch.Tensor,
    keyword_embeddings: Optional[torch.Tensor],
    *,
    keyword_total_weight: float,
    num_steps: int = 80,
    learning_rate: float = 0.1,
) -> torch.Tensor:
    """
    Explicitly optimize the inversion target embedding with a joint cosine objective.

    Objective:
      maximize (1-lambda) * cos(z, noisy) + lambda * mean_k cos(z, kw_k)
    where lambda = keyword_total_weight.

    Notes:
    - If no keywords are provided, this returns normalized noisy embedding.
    - This is a train-free optimization in embedding space only.
    """

    # Clamp lambda to a valid convex trade-off range.
    lambda_kw = max(0.0, min(1.0, float(keyword_total_weight)))

    # If no keyword info is available, target should stay aligned with noisy embedding.
    if keyword_embeddings is None or keyword_embeddings.numel() == 0 or lambda_kw <= 0.0:
        return l2_normalize(noisy_embedding)

    noisy_ref = l2_normalize(noisy_embedding.detach())
    kw_ref = l2_normalize(keyword_embeddings.detach())

    # Start from noisy embedding and optimize z directly.
    z = noisy_embedding.detach().clone().requires_grad_(True)
    optimizer = torch.optim.Adam([z], lr=float(learning_rate))

    for _ in range(max(1, int(num_steps))):
        optimizer.zero_grad(set_to_none=True)
        z_norm = l2_normalize(z)

        cos_noisy = F.cosine_similarity(z_norm, noisy_ref, dim=-1).mean()
        cos_keywords = F.cosine_similarity(
            kw_ref, z_norm.expand_as(kw_ref), dim=-1
        ).mean()
        objective = (1.0 - lambda_kw) * cos_noisy + lambda_kw * cos_keywords

        loss = -objective
        loss.backward()
        optimizer.step()

    return l2_normalize(z.detach())


@torch.no_grad()
def invert_vec2text(
    corrector: Any,
    embedding_1xD: torch.Tensor,
    *,
    num_steps: int = 40,
    sequence_beam_width: int = 0,
    min_length: Optional[int] = None,
    max_length: Optional[int] = None,
    must_include_phrases: Optional[List[str]] = None,
    num_beams: int = 6,
) -> str:
    """Decode an embedding back to text using vec2text."""

    embedding_1xD = embedding_1xD.to(next(corrector.model.parameters()).device)

    generation_kwargs = copy.copy(getattr(corrector, "gen_kwargs", {}))
    generation_kwargs["do_sample"] = False
    generation_kwargs["early_stopping"] = False

    if min_length is not None:
        generation_kwargs["min_length"] = int(min_length)
    if max_length is not None:
        generation_kwargs["max_length"] = int(max_length)

    if must_include_phrases:
        generation_kwargs["num_beams"] = max(int(num_beams), 2)
        force_words_ids: List[List[int]] = []
        for phrase in must_include_phrases:
            token_ids = corrector.tokenizer(phrase, add_special_tokens=False).input_ids
            if len(token_ids) == 0:
                raise ValueError(f"Phrase tokenized to empty list: {phrase!r}")
            force_words_ids.append(token_ids)
        generation_kwargs["force_words_ids"] = force_words_ids
    else:
        generation_kwargs["num_beams"] = 1

    corrector.return_best_hypothesis = sequence_beam_width > 0

    generated_ids = corrector.generate(
        inputs={"frozen_embeddings": embedding_1xD},
        generation_kwargs=generation_kwargs,
        num_recursive_steps=num_steps,
        sequence_beam_width=sequence_beam_width,
    )
    return corrector.tokenizer.batch_decode(generated_ids, skip_special_tokens=True)[0]


def cosine_sim(a: torch.Tensor, b: torch.Tensor) -> float:
    """Compute cosine similarity between two same-shape vectors/tensors."""

    return float(F.cosine_similarity(a, b, dim=-1).item())


def cos_to_keywords(
    recovered_emb: torch.Tensor,
    keyword_embs: Optional[torch.Tensor],
) -> Tuple[float, float]:
    """Return mean/max cosine similarity from recovered embedding to keyword embeddings."""

    if keyword_embs is None or keyword_embs.numel() == 0:
        return float("nan"), float("nan")
    sims = F.cosine_similarity(keyword_embs, recovered_emb.expand_as(keyword_embs), dim=-1)
    return float(sims.mean().item()), float(sims.max().item())


