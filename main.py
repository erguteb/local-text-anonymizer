def anonymize_localized(
    private_text: str,
    keywords: List[str],
    sigma: float,
    corrector: Any,
    *,
    device: str = "cpu",
    keyword_total_weight: float = 0.35,
    noise_seed: int = 0,
    embed_max_length: int = 128,
    keyword_embed_max_length: int = 32,
    num_steps: int = 40,
    sequence_beam_width: int = 0,
    min_length: Optional[int] = 6,
    max_length: Optional[int] = 40,
    joint_opt_steps: int = 80,
    joint_opt_lr: float = 0.1,
) -> AnonymizationResult:
    """
    Anonymize one text segment by optimizing a joint embedding objective, then inverting.
    """

    clean = embed_texts(corrector, [private_text], max_length=embed_max_length, device=device)
    noisy = add_gaussian_noise(clean, noise_std=float(sigma), seed=int(noise_seed))

    cleaned_keywords = [k.strip() for k in keywords if k and k.strip()]
    keyword_embs: Optional[torch.Tensor] = None
    target_embedding = l2_normalize(noisy)
    if len(cleaned_keywords) > 0:
        keyword_embs = embed_texts(
            corrector,
            cleaned_keywords,
            max_length=keyword_embed_max_length,
            device=device,
        )
        target_embedding = optimize_joint_target_embedding(
            noisy_embedding=noisy,
            keyword_embeddings=keyword_embs,
            keyword_total_weight=float(keyword_total_weight),
            num_steps=int(joint_opt_steps),
            learning_rate=float(joint_opt_lr),
        )
    else:
        target_embedding = optimize_joint_target_embedding(
            noisy_embedding=noisy,
            keyword_embeddings=None,
            keyword_total_weight=float(keyword_total_weight),
            num_steps=int(joint_opt_steps),
            learning_rate=float(joint_opt_lr),
        )

    anonymized_text = invert_vec2text(
        corrector,
        target_embedding,
        num_steps=int(num_steps),
        sequence_beam_width=int(sequence_beam_width),
        min_length=min_length,
        max_length=max_length,
        must_include_phrases=None,
    )

    return AnonymizationResult(
        anonymized_text=anonymized_text,
        mixed_target_embedding=target_embedding.detach().cpu(),
        noisy_embedding=noisy.detach().cpu(),
        clean_embedding=clean.detach().cpu(),
        keyword_embeddings=keyword_embs.detach().cpu() if keyword_embs is not None else None,
    )


def anonymize_localized_sentence_wise(
    private_text: str,
    keywords: List[str],
    sigma: float,
    corrector: Any,
    *,
    device: str = "cpu",
    keyword_total_weight: float = 0.35,
    noise_seed: int = 0,
    embed_max_length: int = 128,
    keyword_embed_max_length: int = 32,
    num_steps: int = 40,
    sequence_beam_width: int = 0,
    min_length: Optional[int] = 6,
    max_length: Optional[int] = 40,
    joint_opt_steps: int = 80,
    joint_opt_lr: float = 0.1,
    max_privacy_chunks: int = 3,
) -> SentenceWiseAnonymizationResult:
    """
    Apply localized anonymization to a small number of semantic chunks.
    """

    source_sentences = chunk_sentences_for_privacy(
        split_sentences_by_period(private_text),
        max_chunks=max_privacy_chunks,
    )
    if not source_sentences:
        return SentenceWiseAnonymizationResult(
            anonymized_text="",
            sentence_results=[],
            source_sentences=[],
        )

    sentence_results: List[AnonymizationResult] = []
    for i, sentence in enumerate(source_sentences):
        result = anonymize_localized(
            private_text=sentence,
            keywords=keywords,
            sigma=sigma,
            corrector=corrector,
            device=device,
            keyword_total_weight=keyword_total_weight,
            noise_seed=int(noise_seed) + i,
            embed_max_length=embed_max_length,
            keyword_embed_max_length=keyword_embed_max_length,
            num_steps=num_steps,
            sequence_beam_width=sequence_beam_width,
            min_length=min_length,
            max_length=max_length,
            joint_opt_steps=joint_opt_steps,
            joint_opt_lr=joint_opt_lr,
        )
        sentence_results.append(result)

    parts = [r.anonymized_text.strip() for r in sentence_results if r.anonymized_text.strip()]
    combined = ". ".join(parts)
    return SentenceWiseAnonymizationResult(
        anonymized_text=combined,
        sentence_results=sentence_results,
        source_sentences=source_sentences,
    )


