def main() -> None:
    """Execute full anonymize -> paraphrase workflow."""

    args = build_arg_parser().parse_args()
    if args.fast:
        args.skip_baseline = True
        args.joint_opt_steps = min(args.joint_opt_steps, 40)
        args.num_steps = min(args.num_steps, 20)
        args.paraphrase_num_beams = min(args.paraphrase_num_beams, 2)
        args.max_privacy_chunks = min(args.max_privacy_chunks, 2)

    keywords = parse_keyword_list(args.keywords)
    fallback_models = parse_model_list(args.paraphrase_fallback_models)
    initial_removals = parse_removal_list(args.remove_info)

    print("\n--- Sensitive text (private input X) ---")
    print(args.text)
    print("\n--- User-defined non-sensitive keywords ---")
    if keywords:
        for i, keyword in enumerate(keywords, start=1):
            print(f"  {i}. {keyword}")
    else:
        print("  (none — inversion uses noisy E(X) only)")

    preprocessed = preprocess_privacy_text(
        args.text,
        keywords=keywords,
        removal_targets=initial_removals,
        max_privacy_chunks=args.max_privacy_chunks,
    )
    print("\n--- Deterministic privacy scrub (pre-embedding) ---")
    print(preprocessed.scrubbed_text or "(empty after scrub)")
    print("Residual text sent into DP path:", preprocessed.residual_text or "(empty residual)")
    print("Expanded removal targets:", preprocessed.expanded_removal_targets or "(none)")
    print("Preserved public keywords:", preprocessed.preserved_keywords or "(none)")
    print("Residual DP release units:", len(preprocessed.residual_segments))

    patch_transformers_constrained_beamsearch_4372()
    torch.manual_seed(0)
    device = "cuda" if torch.cuda.is_available() else "cpu"
    print("\nDevice:", device)

    corrector = load_vec2text_corrector(
        device=device,
        inversion_model_name=args.vec2text_inversion_model,
        corrector_model_name=args.vec2text_corrector_model,
        cache_dir=args.vec2text_cache_dir,
    )

    # Main path: keyword-guided anonymization (used for downstream final output).
    sentence_wise = anonymize_localized_sentence_wise(
        private_text=preprocessed.residual_text,
        keywords=keywords,
        sigma=args.sigma,
        corrector=corrector,
        device=device,
        keyword_total_weight=args.keyword_total_weight,
        joint_opt_steps=args.joint_opt_steps,
        joint_opt_lr=args.joint_opt_lr,
        noise_seed=args.noise_seed,
        num_steps=args.num_steps,
        min_length=args.min_length,
        max_length=args.max_length,
        max_privacy_chunks=args.max_privacy_chunks,
    )

    sentence_wise_no_keywords: Optional[SentenceWiseAnonymizationResult] = None
    if not args.skip_baseline:
        sentence_wise_no_keywords = anonymize_localized_sentence_wise(
            private_text=preprocessed.residual_text,
            keywords=[],
            sigma=args.sigma,
            corrector=corrector,
            device=device,
            keyword_total_weight=args.keyword_total_weight,
            joint_opt_steps=args.joint_opt_steps,
            joint_opt_lr=args.joint_opt_lr,
            noise_seed=args.noise_seed,
            num_steps=args.num_steps,
            min_length=args.min_length,
            max_length=args.max_length,
            max_privacy_chunks=args.max_privacy_chunks,
        )

    accountant = estimate_metric_dp_privacy_cost(
        num_releases=len(sentence_wise.source_sentences),
        sigma=args.sigma,
        delta=args.dp_delta,
        sensitivity=args.dp_sensitivity,
    )

    print("\n--- Per-chunk anonymized (before join) ---")
    if sentence_wise_no_keywords is None:
        for i, (src, result_kw) in enumerate(
            zip(sentence_wise.source_sentences, sentence_wise.sentence_results),
            start=1,
        ):
            print(f"  [{i}] (source) {src}")
            print(f"      (with keywords)    {result_kw.anonymized_text.strip()}")
    else:
        for i, (src, result_kw, result_no_kw) in enumerate(
            zip(
                sentence_wise.source_sentences,
                sentence_wise.sentence_results,
                sentence_wise_no_keywords.sentence_results,
            ),
            start=1,
        ):
            print(f"  [{i}] (source) {src}")
            print(f"      (with keywords)    {result_kw.anonymized_text.strip()}")
            print(f"      (without keywords) {result_no_kw.anonymized_text.strip()}")

    print("\n--- Anonymized output (combined, with keywords) ---")
    print(sentence_wise.anonymized_text)
    if sentence_wise_no_keywords is not None:
        print("\n--- Baseline output (combined, without keywords) ---")
        print(sentence_wise_no_keywords.anonymized_text)
    print("\n--- Metric-DP privacy accountant (zCDP -> (epsilon, delta)-DP) ---")
    print("  scope: residual ambiguous content only")
    print("  deterministic scrub outside DP:", bool(preprocessed.expanded_removal_targets))
    print("  preserved public keywords outside DP:", bool(preprocessed.preserved_keywords))
    print("  num_releases (residual chunks):", accountant.num_releases)
    print("  sigma:", accountant.sigma)
    print("  sensitivity:", accountant.sensitivity)
    print("  delta:", accountant.delta)
    print("  rho_single:", accountant.rho_single)
    print("  rho_total:", accountant.rho_total)
    print("  epsilon_estimate:", accountant.epsilon)
    if accountant.epsilon > 10:
        print("  warning: epsilon > 10, which is a weak privacy regime for the residual release.")

    print("\nLLM backend for paraphrase/removal:", args.llm_backend)
    print("Primary paraphrase/removal model:", args.paraphrase_model)
    if fallback_models:
        print("Fallback models:", fallback_models)
    if args.llm_backend == "hf" and args.hf_cache_dir:
        print("Hugging Face cache dir:", args.hf_cache_dir)
    if args.llm_backend == "ollama":
        print("Ollama base URL:", args.ollama_base_url)

    paraphrase_result = paraphrase_with_open_source_llm(
        sentence_wise.anonymized_text,
        model_name=args.paraphrase_model,
        device=device,
        max_input_tokens=args.paraphrase_max_input_tokens,
        max_new_tokens=args.paraphrase_max_new_tokens,
        num_beams=args.paraphrase_num_beams,
        paraphrase_by_sentence=args.paraphrase_by_sentence,
        fallback_models=fallback_models,
        cache_dir=args.hf_cache_dir,
        keywords=keywords,
        llm_backend=args.llm_backend,
        ollama_base_url=args.ollama_base_url,
    )

    print("\n--- Paraphrased output (human-like prompt style) ---")
    print(paraphrase_result.paraphrased_text)
    print("\nParaphrase model details:")
    print("  model_name:", paraphrase_result.model_name)
    print("  model_type:", paraphrase_result.model_type)

    refined_output = paraphrase_result.paraphrased_text
    current_model_name = paraphrase_result.model_name

    # Optional one-shot removal from CLI argument.
    if initial_removals:
        print("\n--- Post-paraphrase refinement: removing requested information ---")
        print("Targets:", preprocessed.expanded_removal_targets)
        removal_result = remove_information_with_open_source_llm(
            refined_output,
            removal_targets=preprocessed.expanded_removal_targets,
            model_name=current_model_name,
            device=device,
            max_input_tokens=args.paraphrase_max_input_tokens,
            max_new_tokens=args.paraphrase_max_new_tokens,
            num_beams=args.paraphrase_num_beams,
            fallback_models=fallback_models,
            cache_dir=args.hf_cache_dir,
            keywords=keywords,
            removal_method=args.removal_method,
            llm_backend=args.llm_backend,
            ollama_base_url=args.ollama_base_url,
        )
        refined_output = removal_result.paraphrased_text
        current_model_name = removal_result.model_name
        print("\n--- Updated output after removal request ---")
        print(refined_output)

    # Interactive additional removal loop.
    if args.interactive_removal:
        rounds = max(0, int(args.max_removal_rounds))
        for round_idx in range(rounds):
            try:
                user_input = input(
                    "\nEnter extra information to remove (comma-separated), "
                    "or press Enter to finish: "
                ).strip()
            except EOFError:
                # Non-interactive shell/session.
                break

            if not user_input:
                break

            removal_targets = parse_removal_list(user_input)
            if not removal_targets:
                continue

            print(f"\n--- Interactive removal round {round_idx + 1} ---")
            print("Targets:", removal_targets)
            removal_result = remove_information_with_open_source_llm(
                refined_output,
                removal_targets=removal_targets,
                model_name=current_model_name,
                device=device,
                max_input_tokens=args.paraphrase_max_input_tokens,
                max_new_tokens=args.paraphrase_max_new_tokens,
                num_beams=args.paraphrase_num_beams,
                fallback_models=fallback_models,
                cache_dir=args.hf_cache_dir,
                keywords=keywords,
                removal_method=args.removal_method,
                llm_backend=args.llm_backend,
                ollama_base_url=args.ollama_base_url,
            )
            refined_output = removal_result.paraphrased_text
            current_model_name = removal_result.model_name
            print("\n--- Updated output ---")
            print(refined_output)

    if looks_like_quality_collapse(
        preprocessed.residual_text,
        refined_output,
        keywords=keywords,
    ):
        refined_output = render_fallback_summary(preprocessed.residual_text, keywords=keywords)
        print("\n--- Residual summary fallback output ---")
        print(refined_output)

    final_output = build_template_fallback_output(
        preprocessed=preprocessed,
        keywords=keywords,
        residual_summary=refined_output,
    )
    print("\n--- Structured final output ---")
    print(final_output)

    print("\n--- Final output after optional removals ---")
    print(final_output)

    print("\n--- Per-chunk embedding checks (side-by-side) ---")
    keyword_eval_embs: Optional[torch.Tensor] = None
    if keywords:
        keyword_eval_embs = embed_texts(
            corrector,
            keywords,
            max_length=32,
            device=device,
        )
    comparison_pairs = (
        zip(sentence_wise.sentence_results, sentence_wise_no_keywords.sentence_results)
        if sentence_wise_no_keywords is not None
        else ((result_kw, result_kw) for result_kw in sentence_wise.sentence_results)
    )
    for i, (result_kw, result_no_kw) in enumerate(comparison_pairs, start=1):
        recovered_kw = embed_texts(
            corrector,
            [result_kw.anonymized_text],
            max_length=128,
            device=device,
        )
        recovered_no_kw = embed_texts(
            corrector,
            [result_no_kw.anonymized_text],
            max_length=128,
            device=device,
        )
        clean_ref = result_kw.clean_embedding.to(device)
        cos_kw = cosine_sim(recovered_kw, clean_ref)
        cos_no_kw = cosine_sim(recovered_no_kw, clean_ref)
        print(
            f"  [{i}] cos(recovered, clean E(sentence)) "
            f"with_kw / without_kw = {cos_kw:.4f} / {cos_no_kw:.4f}"
        )
        if keyword_eval_embs is not None:
            kw_mean_with, kw_max_with = cos_to_keywords(
                recovered_kw,
                keyword_eval_embs,
            )
            kw_mean_no, kw_max_no = cos_to_keywords(
                recovered_no_kw,
                keyword_eval_embs,
            )
            print(
                "       kw_cos_mean/max with_kw / without_kw = "
                f"{kw_mean_with:.4f}/{kw_max_with:.4f} / {kw_mean_no:.4f}/{kw_max_no:.4f}"
            )


if __name__ == "__main__":
    main()
