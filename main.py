"""
Self-contained sentence-wise anonymization + paraphrasing pipeline.

This script has no dependency on local helper modules such as `utils.py`.
Everything required for:
  1) Vec2Text embedding inversion-based anonymization,
  2) sentence-wise processing,
  3) LLM paraphrasing (default: local Ollama HTTP API; optional: Hugging Face in-process)

Default LLM path is Ollama-first: set --llm-backend hf only if you want Transformers
paraphrase models instead of a local Ollama server.

Privacy design follows three layers:
  - Layer 1 (most-sensitive): user-specified information removed in final refinement.
  - Layer 2 (least-sensitive): user-specified releasable keywords guide inversion.
  - Layer 3 (unclear sensitivity): remaining content represented as sentence embedding
    and perturbed with metric-DP style Gaussian noise before inversion.
"""

