def load_paraphraser_model(
    model_name: str,
    device: str,
    *,
    cache_dir: Optional[str] = None,
) -> Tuple[Any, Any, str]:
    """
    Load paraphraser as seq2seq if available; otherwise fallback to causal LM.
    """

    tokenizer = AutoTokenizer.from_pretrained(model_name, cache_dir=cache_dir)
    if tokenizer.pad_token is None and tokenizer.eos_token is not None:
        tokenizer.pad_token = tokenizer.eos_token

    try:
        model = AutoModelForSeq2SeqLM.from_pretrained(model_name, cache_dir=cache_dir).to(device).eval()
        return tokenizer, model, "seq2seq"
    except Exception:
        model = AutoModelForCausalLM.from_pretrained(model_name, cache_dir=cache_dir).to(device).eval()
        if model.config.pad_token_id is None and tokenizer.pad_token_id is not None:
            model.config.pad_token_id = tokenizer.pad_token_id
        return tokenizer, model, "causal"


def load_paraphraser_model_with_fallback(
    model_name: str,
    fallback_models: List[str],
    device: str,
    *,
    cache_dir: Optional[str] = None,
) -> Tuple[Any, Any, str, str]:
    """Try primary paraphraser model first, then fallback model list."""

    candidates: List[str] = []
    for name in [model_name] + list(fallback_models):
        if name and name not in candidates:
            candidates.append(name)

    last_error: Optional[Exception] = None
    for i, candidate in enumerate(candidates, start=1):
        print(f"Paraphraser candidate [{i}/{len(candidates)}]: {candidate}")
        try:
            tokenizer, model, model_type = load_paraphraser_model(
                candidate,
                device,
                cache_dir=cache_dir,
            )
            if candidate != model_name:
                print(f"Falling back from {model_name} to {candidate}.")
            return tokenizer, model, model_type, candidate
        except Exception as exc:  # noqa: BLE001
            last_error = exc
            print(f"Could not load {candidate}: {exc}")

    raise RuntimeError("Failed to load paraphrase model and all fallbacks.") from last_error


def ollama_generate(
    *,
    base_url: str,
    model: str,
    prompt: str,
    num_predict: int,
    temperature: float = 0.0,
    timeout_sec: float = 600.0,
) -> str:
    """
    Call a local Ollama server HTTP API (/api/generate).

    Requires Ollama running (e.g. `ollama serve`) and the model pulled (`ollama pull <model>`).
    """

    url = base_url.rstrip("/") + "/api/generate"
    payload = {
        "model": model,
        "prompt": prompt,
        "stream": False,
        "options": {
            "num_predict": int(num_predict),
            "temperature": float(temperature),
        },
    }
    data = json.dumps(payload).encode("utf-8")
    request = urllib.request.Request(
        url,
        data=data,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urllib.request.urlopen(request, timeout=timeout_sec) as response:
            body = json.loads(response.read().decode("utf-8"))
    except urllib.error.URLError as exc:
        raise RuntimeError(
            f"Ollama request failed ({url}). Is Ollama running? Error: {exc}"
        ) from exc

    return str(body.get("response", "")).strip()


def paraphrase_with_ollama(
    text: str,
    *,
    model_name: str,
    fallback_models: List[str],
    ollama_base_url: str,
    max_new_tokens: int,
    paraphrase_by_sentence: bool,
    keywords: Optional[List[str]],
) -> ParaphraseResult:
    """
    Paraphrase via Ollama instead of in-process Hugging Face models.
    Uses instruction-style prompts (same as seq2seq path in build_paraphrase_prompt).
    """

    candidates: List[str] = []
    for name in [model_name] + list(fallback_models):
        if name and name not in candidates:
            candidates.append(name)

    last_error: Optional[Exception] = None
    for i, candidate in enumerate(candidates, start=1):
        print(f"Ollama paraphrase candidate [{i}/{len(candidates)}]: {candidate}")
        try:
            source_chunks = split_sentences_by_period(text) if paraphrase_by_sentence else [text]
            if len(source_chunks) == 0:
                source_chunks = [text]

            paraphrased_chunks: List[str] = []
            for chunk in source_chunks:
                sentence_count_hint = max(1, len(split_sentences_by_period(chunk)))
                prompt = build_paraphrase_prompt(
                    text=chunk,
                    sentence_count_hint=sentence_count_hint,
                    model_type="seq2seq",
                    keywords=keywords,
                )
                first_pass = ollama_generate(
                    base_url=ollama_base_url,
                    model=candidate,
                    prompt=prompt,
                    num_predict=max_new_tokens,
                    temperature=0.0,
                )
                if violates_local_rewrite_constraints(chunk, first_pass, keywords=keywords):
                    second_pass = ollama_generate(
                        base_url=ollama_base_url,
                        model=candidate,
                        prompt=prompt,
                        num_predict=max_new_tokens,
                        temperature=0.85,
                    )
                    final_chunk = (
                        second_pass
                        if not violates_local_rewrite_constraints(
                            chunk,
                            second_pass,
                            keywords=keywords,
                        )
                        else render_fallback_summary(chunk, keywords=keywords)
                    )
                else:
                    final_chunk = first_pass
                paraphrased_chunks.append(final_chunk.strip())

            paraphrased_text = ". ".join([c for c in paraphrased_chunks if c]).strip()
            if not paraphrased_text:
                paraphrased_text = text.strip()

            return ParaphraseResult(
                model_name=candidate,
                model_type="ollama",
                input_text=text,
                paraphrased_text=paraphrased_text,
            )
        except Exception as exc:  # noqa: BLE001
            last_error = exc
            print(f"Could not use Ollama model {candidate}: {exc}")

    raise RuntimeError("Failed to paraphrase with Ollama and all fallbacks.") from last_error


def build_paraphrase_prompt(
    text: str,
    sentence_count_hint: int,
    model_type: str,
    keywords: Optional[List[str]] = None,
) -> str:
    """
    Build instruction prompt for constrained local rewriting.
    """

    keyword_line = "Keywords: (none)"
    if keywords:
        keyword_line = f"Approved public keywords to keep if already present: {', '.join(keywords)}"

    if model_type == "seq2seq":
        return (
            "Rewrite the anonymized text below into a short, neutral, locally safe summary. "
            "Preserve placeholders such as [PERSON], [AGE], [ORG], and [LOCATION] exactly. "
            "Do not add names, numbers, organizations, addresses, dates, or locations that are not "
            "already present. Keep approved public keywords only if they are already supported by the "
            f"text. Keep roughly {sentence_count_hint} sentence(s). If the text is degraded, simplify "
            "it instead of inventing missing facts.\n\n"
            f"{keyword_line}\n\nText: {text}"
        )
    return (
        "Rewrite the anonymized text below into a short, neutral, privacy-safe summary. Preserve "
        "placeholders such as [PERSON], [AGE], [ORG], and [LOCATION] exactly. Do not add names, "
        "numbers, organizations, addresses, dates, or locations that are not already present. "
        f"Keep roughly {sentence_count_hint} sentence(s). If the text is degraded, simplify it "
        "instead of inventing facts.\n\n"
        f"{keyword_line}\n\nAnonymized text: {text}\n\nRewritten prompt:"
    )


def is_too_short_or_collapsed(source_text: str, candidate_text: str) -> bool:
    """
    Heuristic guard against severe collapse (e.g. single short sentence summaries).
    """

    if not candidate_text.strip():
        return True
    char_ratio = len(candidate_text.strip()) / max(1, len(source_text.strip()))
    source_sentences = max(1, len(split_sentences_by_period(source_text)))
    candidate_sentences = max(1, len(split_sentences_by_period(candidate_text)))
    if char_ratio < 0.45:
        return True
    if source_sentences >= 3 and candidate_sentences < 2:
        return True
    return False


def has_unapproved_numeric_content(
    source_text: str,
    candidate_text: str,
    keywords: Optional[Sequence[str]] = None,
) -> bool:
    """Reject rewrites that introduce novel numbers not present in the source."""

    source_numbers = set(re.findall(r"\b\d+(?:[.,]\d+)?\b", source_text))
    candidate_numbers = set(re.findall(r"\b\d+(?:[.,]\d+)?\b", candidate_text))
    return not candidate_numbers.issubset(source_numbers)


def has_unapproved_capitalized_entities(
    source_text: str,
    candidate_text: str,
    keywords: Optional[Sequence[str]] = None,
) -> bool:
    """Reject rewrites that invent new entity-like capitalized tokens."""

    allowed = {"I", "UK", "London"}
    for keyword in keywords or []:
        for token in re.findall(r"\b[A-Z][A-Za-z0-9_-]*\b", keyword):
            allowed.add(token)
    for token in re.findall(r"\[[A-Z]+\]", source_text):
        allowed.add(token)
    for token in re.findall(r"\b[A-Z][A-Za-z0-9_-]*\b", source_text):
        allowed.add(token)
    candidate_tokens = set(re.findall(r"\[[A-Z]+\]|\b[A-Z][A-Za-z0-9_-]*\b", candidate_text))
    return any(token not in allowed for token in candidate_tokens)


def leaks_removal_targets(text: str, removal_targets: Sequence[str]) -> bool:
    """Check whether requested removals still appear after a rewrite/removal pass."""

    for target in removal_targets:
        cleaned = target.strip()
        if not cleaned or cleaned.startswith("[") and cleaned.endswith("]"):
            continue
        if re.search(re.escape(cleaned), text, flags=re.IGNORECASE):
            return True
    return False


def violates_local_rewrite_constraints(
    source_text: str,
    candidate_text: str,
    *,
    keywords: Optional[Sequence[str]] = None,
) -> bool:
    """Guard against invented facts in the local rewrite stage."""

    if is_too_short_or_collapsed(source_text, candidate_text):
        return True
    if has_unapproved_numeric_content(source_text, candidate_text, keywords):
        return True
    if has_unapproved_capitalized_entities(source_text, candidate_text, keywords):
        return True
    return False


def build_removal_prompt(
    text: str,
    removal_targets: List[str],
    model_type: str,
    keywords: Optional[List[str]] = None,
) -> str:
    """
    Build prompt for removing additional user-specified information.

    The model is instructed to:
    - remove requested details,
    - keep the rest coherent and human-like,
    - avoid adding new facts,
    - still consider keyword topics where compatible.
    """

    removal_line = ", ".join(removal_targets)
    keyword_line = "Keywords: (none)"
    if keywords:
        keyword_line = f"Keywords to still consider when possible: {', '.join(keywords)}"

    if model_type == "seq2seq":
        return (
            "Revise the text below by replacing the requested information with the matching privacy "
            "placeholders. Preserve all remaining text structure as much as possible. Do not add "
            "new facts, names, numbers, or locations.\n\n"
            f"Information to replace: {removal_line}\n"
            f"{keyword_line}\n\n"
            f"Text: {text}"
        )
    return (
        "Revise the text below by replacing the requested information with matching privacy "
        "placeholders. Preserve all remaining text structure as much as possible. Do not add "
        "new facts, names, numbers, or locations.\n\n"
        f"Information to replace: {removal_line}\n"
        f"{keyword_line}\n\n"
        f"Text: {text}\n\nRevised text:"
    )


@torch.no_grad()
def generate_paraphrase_once(
    *,
    tokenizer: Any,
    model: Any,
    model_type: str,
    text: str,
    device: str,
    max_input_tokens: int,
    max_new_tokens: int,
    num_beams: int,
    do_sample: bool,
    keywords: Optional[List[str]] = None,
) -> str:
    """
    Generate one paraphrase attempt for one text chunk.
    """

    sentence_count_hint = max(1, len(split_sentences_by_period(text)))
    prompt = build_paraphrase_prompt(
        text=text,
        sentence_count_hint=sentence_count_hint,
        model_type=model_type,
        keywords=keywords,
    )
    model_inputs = tokenizer(
        prompt,
        return_tensors="pt",
        truncation=True,
        max_length=max_input_tokens,
    ).to(device)

    if model_type == "seq2seq":
        outputs = model.generate(
            **model_inputs,
            do_sample=do_sample,
            temperature=0.85 if do_sample else None,
            top_p=0.92 if do_sample else None,
            num_beams=max(1, int(num_beams)) if not do_sample else 1,
            no_repeat_ngram_size=3,
            repetition_penalty=1.05,
            max_new_tokens=max_new_tokens,
            min_new_tokens=max(12, min(64, max_new_tokens // 3)),
            early_stopping=True,
        )
        return tokenizer.decode(outputs[0], skip_special_tokens=True).strip()

    prompt_len = model_inputs["input_ids"].shape[1]
    outputs = model.generate(
        **model_inputs,
        do_sample=do_sample,
        temperature=0.85 if do_sample else None,
        top_p=0.92 if do_sample else None,
        num_beams=max(2, int(num_beams)) if not do_sample else 1,
        no_repeat_ngram_size=3,
        repetition_penalty=1.08,
        max_new_tokens=max_new_tokens,
        min_new_tokens=max(12, min(64, max_new_tokens // 3)),
        early_stopping=True,
        pad_token_id=tokenizer.pad_token_id,
        eos_token_id=tokenizer.eos_token_id,
    )
    gen_ids = outputs[0][prompt_len:]
    return tokenizer.decode(gen_ids, skip_special_tokens=True).strip()


def render_fallback_summary(text: str, keywords: Optional[Sequence[str]] = None) -> str:
    """
    Deterministic fallback when local generation is degraded.

    This intentionally avoids inventing new details; it just normalizes the
    scrubbed text into a short stable summary.
    """

    cleaned = normalize_whitespace(text)
    if not cleaned:
        return ""
    chunks = split_sentences_by_period(cleaned)
    if not chunks:
        return cleaned
    limited = " ".join(chunks[:2]).strip()
    return limited


def extract_content_words(text: str) -> List[str]:
    """Return simple content-word tokens for overlap checks."""

    stopwords = {
        "the",
        "and",
        "for",
        "that",
        "with",
        "this",
        "from",
        "into",
        "where",
        "when",
        "have",
        "just",
        "your",
        "after",
        "about",
        "they",
        "them",
        "then",
        "than",
        "there",
        "their",
        "would",
        "could",
        "should",
        "want",
        "need",
        "looking",
        "please",
    }
    words = re.findall(r"\b[a-z]{3,}\b", text.lower())
    return [word for word in words if word not in stopwords]


def lexical_overlap_ratio(source_text: str, candidate_text: str) -> float:
    """Approximate semantic faithfulness with content-word overlap."""

    source_words = set(extract_content_words(source_text))
    candidate_words = set(extract_content_words(candidate_text))
    if not source_words or not candidate_words:
        return 0.0
    return len(source_words & candidate_words) / max(1, len(source_words))


def looks_like_quality_collapse(
    source_text: str,
    candidate_text: str,
    *,
    keywords: Optional[Sequence[str]] = None,
) -> bool:
    """Detect outputs that are fluent-looking but semantically ungrounded."""

    candidate = normalize_whitespace(candidate_text)
    if not candidate:
        return True
    if violates_local_rewrite_constraints(source_text, candidate, keywords=keywords):
        return True
    if ".." in candidate or ";;" in candidate:
        return True
    if lexical_overlap_ratio(source_text, candidate) < 0.2:
        return True
    return False


def select_request_sentence(sentences: Sequence[str]) -> str:
    """Pick the most request-like sentence from scrubbed text."""

    request_markers = ("want", "need", "looking", "recommend", "help", "find", "search")
    for sentence in sentences:
        lowered = sentence.lower()
        if any(marker in lowered for marker in request_markers):
            return sentence.strip()
    return sentences[-1].strip() if sentences else ""


def build_template_fallback_output(
    *,
    preprocessed: PreprocessedPrivacyText,
    keywords: Sequence[str],
    residual_summary: Optional[str] = None,
) -> str:
    """
    Build a deterministic final prompt from scrubbed text and approved public slots.
    """

    del keywords
    summary_text = restore_safe_slot_placeholders(
        residual_summary if residual_summary is not None else preprocessed.scrubbed_text,
        safe_task_keywords=preprocessed.safe_task_keywords,
        safe_location_keywords=preprocessed.safe_location_keywords,
        safe_preference_keywords=preprocessed.safe_preference_keywords,
    )
    sentences = split_sentences_by_period(summary_text)
    context_sentence = sentences[0].strip() if sentences else ""
    request_sentence = select_request_sentence(sentences)
    if request_sentence == context_sentence and len(sentences) > 1:
        request_sentence = sentences[-1].strip()

    parts: List[str] = []
    if context_sentence:
        parts.append(f"Context: {context_sentence}")
    if request_sentence:
        parts.append(f"Request: {request_sentence}")
    if preprocessed.safe_task_keywords:
        parts.append("Task: " + ", ".join(preprocessed.safe_task_keywords))
    if preprocessed.safe_location_keywords:
        parts.append("Location: " + ", ".join(preprocessed.safe_location_keywords))
    if preprocessed.safe_preference_keywords:
        parts.append("Preferences: " + ", ".join(preprocessed.safe_preference_keywords))

    if not parts:
        return summary_text
    return " ".join(parts)


@torch.no_grad()
def paraphrase_with_open_source_llm(
    text: str,
    *,
    model_name: str,
    device: str,
    max_input_tokens: int = 512,
    max_new_tokens: int = 128,
    num_beams: int = 4,
    paraphrase_by_sentence: bool = True,
    fallback_models: Optional[List[str]] = None,
    cache_dir: Optional[str] = None,
    keywords: Optional[List[str]] = None,
    llm_backend: str = "hf",
    ollama_base_url: str = "http://127.0.0.1:11434",
) -> ParaphraseResult:
    """
    Paraphrase anonymized text with an open-source model.

    Strategy:
    - Optionally paraphrase per sentence (default) for better stability.
    - If first deterministic pass is too short/collapsed, retry with sampling.
    - If still poor, keep original chunk.

    llm_backend:
    - "hf": Hugging Face Transformers in-process (downloads weights).
    - "ollama": local Ollama HTTP API (no HF paraphrase download).
    """

    if llm_backend == "ollama":
        return paraphrase_with_ollama(
            text,
            model_name=model_name,
            fallback_models=fallback_models or [],
            ollama_base_url=ollama_base_url,
            max_new_tokens=max_new_tokens,
            paraphrase_by_sentence=paraphrase_by_sentence,
            keywords=keywords,
        )

    tokenizer, model, model_type, loaded_model_name = load_paraphraser_model_with_fallback(
        model_name=model_name,
        fallback_models=fallback_models or [],
        device=device,
        cache_dir=cache_dir,
    )

    source_chunks = split_sentences_by_period(text) if paraphrase_by_sentence else [text]
    if len(source_chunks) == 0:
        source_chunks = [text]

    paraphrased_chunks: List[str] = []
    for chunk in source_chunks:
        first_pass = generate_paraphrase_once(
            tokenizer=tokenizer,
            model=model,
            model_type=model_type,
            text=chunk,
            device=device,
            max_input_tokens=max_input_tokens,
            max_new_tokens=max_new_tokens,
            num_beams=num_beams,
            do_sample=False,
            keywords=keywords,
        )
        if violates_local_rewrite_constraints(chunk, first_pass, keywords=keywords):
            second_pass = generate_paraphrase_once(
                tokenizer=tokenizer,
                model=model,
                model_type=model_type,
                text=chunk,
                device=device,
                max_input_tokens=max_input_tokens,
                max_new_tokens=max_new_tokens,
                num_beams=num_beams,
                do_sample=True,
                keywords=keywords,
            )
            final_chunk = (
                second_pass
                if not violates_local_rewrite_constraints(chunk, second_pass, keywords=keywords)
                else render_fallback_summary(chunk, keywords=keywords)
            )
        else:
            final_chunk = first_pass
        paraphrased_chunks.append(final_chunk.strip())

    paraphrased_text = ". ".join([chunk for chunk in paraphrased_chunks if chunk]).strip()
    if not paraphrased_text:
        paraphrased_text = text.strip()

    return ParaphraseResult(
        model_name=loaded_model_name,
        model_type=model_type,
        input_text=text,
        paraphrased_text=paraphrased_text,
    )


@torch.no_grad()
def remove_information_with_open_source_llm(
    text: str,
    *,
    removal_targets: List[str],
    model_name: str,
    device: str,
    max_input_tokens: int = 512,
    max_new_tokens: int = 128,
    num_beams: int = 4,
    fallback_models: Optional[List[str]] = None,
    cache_dir: Optional[str] = None,
    keywords: Optional[List[str]] = None,
    removal_method: str = "literal",
    llm_backend: str = "hf",
    ollama_base_url: str = "http://127.0.0.1:11434",
) -> ParaphraseResult:
    """
    Remove user-specified information from already-paraphrased text and regenerate output.
    """

    if len(removal_targets) == 0:
        return ParaphraseResult(
            model_name=model_name,
            model_type="unknown",
            input_text=text,
            paraphrased_text=text,
        )

    if removal_method == "literal":
        revised_text = replace_targets_literal(text, removal_targets)
        return ParaphraseResult(
            model_name=model_name,
            model_type="literal",
            input_text=text,
            paraphrased_text=revised_text,
        )

    if removal_method not in {"llm", "literal_then_llm"}:
        raise ValueError(
            "removal_method must be one of: literal, llm, literal_then_llm"
        )

    working_text = text
    if removal_method == "literal_then_llm":
        working_text = replace_targets_literal(text, removal_targets)

    if llm_backend == "ollama":
        candidates: List[str] = []
        for name in [model_name] + list(fallback_models or []):
            if name and name not in candidates:
                candidates.append(name)
        prompt = build_removal_prompt(
            text=working_text,
            removal_targets=removal_targets,
            model_type="seq2seq",
            keywords=keywords,
        )
        last_ollama_error: Optional[Exception] = None
        for i, candidate in enumerate(candidates, start=1):
            print(f"Ollama removal candidate [{i}/{len(candidates)}]: {candidate}")
            try:
                revised_text = ollama_generate(
                    base_url=ollama_base_url,
                    model=candidate,
                    prompt=prompt,
                    num_predict=max_new_tokens,
                    temperature=0.0,
                )
                if not revised_text.strip():
                    revised_text = working_text
                if leaks_removal_targets(revised_text, removal_targets):
                    revised_text = replace_targets_literal(working_text, removal_targets)
                return ParaphraseResult(
                    model_name=candidate,
                    model_type="ollama",
                    input_text=text,
                    paraphrased_text=revised_text.strip(),
                )
            except Exception as exc:  # noqa: BLE001
                last_ollama_error = exc
                print(f"Could not use Ollama model {candidate}: {exc}")
        raise RuntimeError(
            "Failed removal step with Ollama and all fallbacks."
        ) from last_ollama_error

    tokenizer, model, model_type, loaded_model_name = load_paraphraser_model_with_fallback(
        model_name=model_name,
        fallback_models=fallback_models or [],
        device=device,
        cache_dir=cache_dir,
    )

    prompt = build_removal_prompt(
        text=working_text,
        removal_targets=removal_targets,
        model_type=model_type,
        keywords=keywords,
    )
    model_inputs = tokenizer(
        prompt,
        return_tensors="pt",
        truncation=True,
        max_length=max_input_tokens,
    ).to(device)

    if model_type == "seq2seq":
        outputs = model.generate(
            **model_inputs,
            do_sample=False,
            num_beams=max(1, int(num_beams)),
            no_repeat_ngram_size=3,
            repetition_penalty=1.05,
            max_new_tokens=max_new_tokens,
            min_new_tokens=max(12, min(64, max_new_tokens // 3)),
            early_stopping=True,
        )
        revised_text = tokenizer.decode(outputs[0], skip_special_tokens=True).strip()
    else:
        prompt_len = model_inputs["input_ids"].shape[1]
        outputs = model.generate(
            **model_inputs,
            do_sample=False,
            num_beams=max(2, int(num_beams)),
            no_repeat_ngram_size=3,
            repetition_penalty=1.08,
            max_new_tokens=max_new_tokens,
            min_new_tokens=max(12, min(64, max_new_tokens // 3)),
            early_stopping=True,
            pad_token_id=tokenizer.pad_token_id,
            eos_token_id=tokenizer.eos_token_id,
        )
        gen_ids = outputs[0][prompt_len:]
        revised_text = tokenizer.decode(gen_ids, skip_special_tokens=True).strip()

    if not revised_text:
        revised_text = working_text
    if leaks_removal_targets(revised_text, removal_targets):
        revised_text = replace_targets_literal(working_text, removal_targets)

    return ParaphraseResult(
        model_name=loaded_model_name,
        model_type=model_type,
        input_text=text,
        paraphrased_text=revised_text,
    )


